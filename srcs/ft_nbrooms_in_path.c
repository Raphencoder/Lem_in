#include "../lem_in.h"

int			ft_nbrooms_in_path(char *path)
{
	int		ret;
	int		i;

	i = 0;
	ret = 1;
	while (path[i])
	{
		if (path[i] == '-')
			ret++;
		i++;
	}
	return (ret);
}
